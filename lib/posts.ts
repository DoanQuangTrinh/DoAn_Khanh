// Kiểu dữ liệu BlogPost
export interface BlogPost {
    id: number;
    slug: string;
    title: string;
    excerpt: string;
    content: string;
    imageUrl: string;
    author: string;
    date: string;
}

// Biến đổi Product → BlogPost
const transformProductToPost = (product: typeof listProducts[0]): BlogPost => {
    const dates = ["25/10/2025", "22/10/2025", "18/10/2025", "15/10/2025", "10/10/2025", "05/10/2025"];

    return {
        id: product.id,
        slug: product.id.toString(),
        title: product.title,
        excerpt: product.description,
        content: product.details.description_details,
        imageUrl: product.details.images[0]?.url || "",
        author: "GreenKids Team",
        date: dates[product.id % dates.length] || "26/10/2025",
    };
};

// Lấy tất cả bài viết
export const getAllPosts = (): BlogPost[] => listProducts.map(transformProductToPost);

// Lấy một bài viết theo ID
export const getPostById = (id: string): BlogPost | undefined => {
    const post = listProducts.find((p) => p.id.toString() === id);
    return post ? transformProductToPost(post) : undefined;
};
